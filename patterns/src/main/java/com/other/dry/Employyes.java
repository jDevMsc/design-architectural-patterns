package com.other.dry;

public class Employyes {

  private String namel;
  private int number;

  public Employyes(String namel, int number) {
    this.namel = namel;
    this.number = number;
  }

  public String getNamel() {
    return namel;
  }

  public void setNamel(String namel) {
    this.namel = namel;
  }

  public int getNumber() {
    return number;
  }

  public void setNumber(int number) {
    this.number = number;
  }
}
